    <h3>  Qu'est-ce qu'une allocation de rentrée scolaire ? (ARS) </h3>
    <h2>
      <span> 
L’ARS (l’Allocation de Rentrée Scolaire) est une aide financière versée par l’état aux familles modestes. Son montant en 2019 est fixé entre 368,84 euros et 402,67 euros en fonction de l’âge de l’enfant.<p><p>
Le versement de l’Allocation de Rentrée Scolaire se fera le 20 août 2019 sur votre compte bancaire. Il s’agit de la date officielle de versement, l’article a été mis à jour.<p><p>

Les nouveaux montants de la prime rentrée scolaire 2019 ont été communiqués. Ils sont similaires à ceux versés l’année passée. L’article a été actualisé selon les montants officiels de l’ARS 2019.
 </span>
    </h2>
